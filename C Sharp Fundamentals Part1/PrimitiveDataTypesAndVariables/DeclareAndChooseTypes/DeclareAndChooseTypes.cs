using System;

class DeclareAndChooseTypes
{
    static void Main()
    {
        sbyte sbyteVariable = -115;
        byte byteVariable = 97;
        ushort ushortVariable = 52130;
        short shortVariable = -10000;
        int intVariable = 4825932;

        Console.WriteLine("sbyteVariable = {0}\n", sbyteVariable);
        Console.WriteLine("byteVariable = {0}\n", byteVariable);
        Console.WriteLine("ushortVariable = {0}\n", ushortVariable);
        Console.WriteLine("shortVariable = {0}\n", shortVariable);
        Console.WriteLine("intVariable = {0}\n", intVariable);
    }
}
